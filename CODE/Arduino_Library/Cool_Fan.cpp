Cool_Fan::Cool_Fan()
{}

void Cool_Fan::begin(uint8_t ConnectPin)
{
	FanPin = ConnectPin;
	pinMode(FanPin,OUTPUT);
	Mode = 1;
	TimerPhase = false;
}
